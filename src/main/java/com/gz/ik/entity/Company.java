package com.gz.ik.entity;

public class Company {
	
	private Integer companyId;
	private String companyName;
	private String companyTel;
	private String companyEmail;
	private String companyAddress;
	private String companyDes;
	
	private CompanyImg companyImg;

	public Integer getCompanyId() {
		return companyId;
	}

	public void setCompanyId(Integer companyId) {
		this.companyId = companyId;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getCompanyTel() {
		return companyTel;
	}

	public void setCompanyTel(String companyTel) {
		this.companyTel = companyTel;
	}

	public String getCompanyEmail() {
		return companyEmail;
	}

	public void setCompanyEmail(String companyEmail) {
		this.companyEmail = companyEmail;
	}

	public String getCompanyAddress() {
		return companyAddress;
	}

	public void setCompanyAddress(String companyAddress) {
		this.companyAddress = companyAddress;
	}

	public String getCompanyDes() {
		return companyDes;
	}

	public void setCompanyDes(String companyDes) {
		this.companyDes = companyDes;
	}

	public CompanyImg getCompanyImg() {
		return companyImg;
	}

	public void setCompanyImg(CompanyImg companyImg) {
		this.companyImg = companyImg;
	}
	
	

}
