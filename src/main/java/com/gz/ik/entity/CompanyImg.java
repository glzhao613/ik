package com.gz.ik.entity;

public class CompanyImg {
	
	private Integer companyImgId;
	private String companyImgPath;
	private String companyImgDes;
	
	public Integer getCompanyImgId() {
		return companyImgId;
	}
	public void setCompanyImgId(Integer companyImgId) {
		this.companyImgId = companyImgId;
	}
	public String getCompanyImgPath() {
		return companyImgPath;
	}
	public void setCompanyImgPath(String companyImgPath) {
		this.companyImgPath = companyImgPath;
	}
	public String getCompanyImgDes() {
		return companyImgDes;
	}
	public void setCompanyImgDes(String companyImgDes) {
		this.companyImgDes = companyImgDes;
	}
	
	

}
